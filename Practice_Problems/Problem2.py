# ----- Implicit Type Conversion -----
# Python automatically converts int to float when needed

num1 = 10
num2 = 5.5

result = num1 + num2
print("Result using implicit type conversion:", result)




a = input("Enter first number: ")
b = input("Enter second number: ")


a = float(a)
b = float(b)

sum_result = a + b
print("Result using explicit type conversion:", sum_result)
