#  Type Casting Example

# Input values with appropriate casting
number1 = int(input("Please enter first number (integer): "))
number2 = int(input("Please enter second number (integer): "))
number3 = float(input("Please enter third number (float): "))

result = number1 + number2
print("The summation of the first two integers is:",result)


result = number1 - number2
print("The subtraction of the first two integers is:",result)

result = number1 * number3
print("The summation of an integer and a float is:",result)

result = number1 / number3
print("The division of an integer and a float is:",result)

result = number1 ** number3
print("The exponentian of an integer and a float is:",result)
