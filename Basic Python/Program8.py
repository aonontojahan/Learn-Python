# Grade Management System
mark = float(input("Please enter your mark: "))

if mark >= 80:
    print("Your grade is A+")

elif mark >= 70:
    print("Your grade is B+")

elif mark >= 60:
    print("Your grade is C+")

elif mark >= 50:
    print("Your grade is D+")

else:
    print("Sorry to say. You fail in the exam")
